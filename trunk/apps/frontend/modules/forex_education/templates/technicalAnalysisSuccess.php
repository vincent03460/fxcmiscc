<p>Technical analysis is the forecasting of future market trends and price movements through the research of market data
    and figures that are presented in charts. It is a perfect fusion of professional knowledge and the different methods
    of research. Technical analysis has the following advantages over fundamental analysis:</p>
<p><strong>(1) Operability:</strong></p>
<p>Any factors affecting the market will eventually be reflected in the price movements which can be plotted on a chart.
    By following the trends depicted by the graphs, we will be able to grasp the momentum of the market. This is
    something that fundamental analysis greatly lacks.</p>
<p><strong>(2) High flexibility:</strong></p>
<p>Technical analysis can be applied to all kinds of speculative markets. Once you have mastered its application, you
    will be able to simultaneously track as many markets as you like. In comparison, the complexity in data acquisition
    for fundamental analysis often overwhelms analysts so much that they are forced to concentrate on a single product
    or market.</p>
<p><strong>(3) Applicable for any time frame:</strong></p>
<p>Technical analysis can be flexibly applied to any time frame. You can apply technical analysis easily whether you are
    tracking price movement within the same day, or doing mid to long-term market analysis. This is different from
    fundamental analysis which is only applicable to macroeconomic studies.</p>
<p><strong>(4) Clear buy/sell indicators:</strong></p>
<p>The biggest advantage of technical analysis is that it is able to determine entry points in the trading process. No
    matter how well you can perform fundamental analysis, technical analysis is still more useful in actual trading.</p>
